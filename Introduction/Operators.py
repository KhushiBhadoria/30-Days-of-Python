'''Logical Operator
and-> return true if both the operands are true
or -> return true if either of the opeerands is true
nor-> return true if operands are false
'''
var1=True
var2=False
print(var1 and var2)# False
print(var1 or var2)# True
print(not var1 )# False
print(not var2 )# True

'''Bitwise Operator
AND & -> The result of AND is 1 only if the both bits are one 
OR | -> The result of OR is 1 if either of the two bits is one
NOT ~ -> It invert all the bits 
XOR ^ -> The result of XOR is one if the two bits are different
'''
a=2
b=3
print(a&b) #2
print(a|b) #3 
print(a^b) #1
print(~b)  #-4
